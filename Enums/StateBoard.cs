namespace TicTacToe.Enums
{
    /// <summary>
    /// Enumeration definindo os 3 estados possíveis das posições do tabuleiro
    /// Void: Posição disponível
    /// X: Posição marcada pelo jogador X (Jogador 1)
    /// O: Posição marcada pelo jogador O (Jogador 2)
    /// </summary>
    public enum StateBoard
    {
        Void = 0,
        X = 1,
        O = 2
    }
}